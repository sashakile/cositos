"""Run benchmark scenarios; report structural metrics per (scenario, variant, scale).

    python examples/benchmarks/run.py [scenario] [scale|all]
    python examples/benchmarks/run.py all                 # every scenario, every scale

Scenarios are modules next to this file exposing ``SCALES`` and ``build`` (see benchlib).
Each (variant, scale) is measured in a **fresh subprocess** — ipywidgets keeps a global
widget registry that cross-contaminates sequential builds. Writes RESULTS.md.
"""

from __future__ import annotations

import importlib
import json
import subprocess
import sys
from dataclasses import asdict
from pathlib import Path

import benchlib

HERE = Path(__file__).parent
SCENARIOS = ["crossfilter", "masterdetail", "form"]


def _measure_isolated(scenario: str, variant: str, scale: str) -> benchlib.Metrics:
    out = subprocess.run(
        [sys.executable, __file__, "--worker", scenario, variant, scale],
        capture_output=True, text=True, cwd=HERE,
    )
    picks = [ln for ln in out.stdout.splitlines() if ln.startswith("{")]
    if not picks:
        raise RuntimeError(f"worker failed for {scenario}/{variant}/{scale}:\n{out.stderr}")
    return benchlib.Metrics(**json.loads(picks[-1]))


def main() -> int:
    if len(sys.argv) >= 5 and sys.argv[1] == "--worker":
        _, _, scenario, variant, scale = sys.argv[:5]
        module = importlib.import_module(scenario)
        print(json.dumps(asdict(benchlib.measure(scenario, module, variant, scale))))
        return 0

    arg_scenario = sys.argv[1] if len(sys.argv) > 1 else "all"
    arg_scale = sys.argv[2] if len(sys.argv) > 2 else "all"
    scenarios = SCENARIOS if arg_scenario in ("all", None) else [arg_scenario]

    lines = [
        "# Benchmark results",
        "",
        "Same logical app per scenario, built two ways (A = naive peer-to-peer ipywidgets, "
        "B = MVU single-model). Each row is measured in an isolated subprocess. "
        "Generated by `run.py` — do not edit.",
        "",
        "- `data_edges` — declared observe/link dependency edges (wiring complexity)",
        "- `storm` — recomputes triggered by ONE user action",
        "- `links_kept` — cross-part LinkModels that survive into the serialized document",
    ]
    print("\n".join(lines))
    for scenario in scenarios:
        module = importlib.import_module(scenario)
        scales = list(module.SCALES) if arg_scale == "all" else [arg_scale]
        for scale in scales:
            header = f"## {scenario} — scale = {scale}  ({module.SCALES[scale].label})"
            print("\n" + header)
            lines += ["", header, "", "```"]
            for variant in getattr(module, "VARIANTS", ("A", "B")):
                m = _measure_isolated(scenario, variant, scale)
                print("  " + m.row())
                lines.append(m.row())
            lines.append("```")

    out = HERE / "RESULTS.md"
    out.write_text("\n".join(lines) + "\n")
    print(f"\nwrote {out}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
